from datetime import datetime

def get_next_bus(timetable, station, current_time):
    bus_times = timetable.get(station)
    if not bus_times:
        return None
    for time_str in bus_times:
        bus_time = datetime.strptime(time_str, "%H:%M").time()
        if bus_time > current_time:
            return bus_time
    return None

def main():
    # 사용자 입력 받기
    line = input("타고 싶은 노선을 입력하세요 (A 또는 B): ").strip().upper()
    station = input("정류장을 입력하세요: ").strip()
    
    # 현재 시간 가져오기
    current_time = datetime.now().time()
    
    # 시간표 선택
    if line == "A":
        timetable = a_timetable
    elif line == "B":
        timetable = b_timetable
    else:
        print("올바른 노선(A 또는 B)을 입력하세요.")
        return
    
    # 다음 버스 시간 계산
    next_bus = get_next_bus(timetable, station, current_time)
    if next_bus:
        print(f"다음 {line} 노선 버스는 {station}에 {next_bus.strftime('%H:%M')}에 도착합니다.")
    else:
        print(f"오늘 남은 {line} 노선 버스가 더 이상 없습니다.")

if __name__ == "__main__":
    main()
